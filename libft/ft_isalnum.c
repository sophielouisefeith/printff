#include "libft.h"

int	ft_isalnum(int c)
{
	int i;

	i = 0;
	if (c >= '0' && c <= '9')
		return (1);
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	return (0);
}
